/*
 * Problem 6. Print Numbers
Write a program to print the numbers 1, 101 and 1001, each at a separate line.
Name the program correctly.
 */


using System;

class PrintNumbers
{
    static void Main()
    {
        int a = 1;
        int b = 101;
        int c = 1001;
        Console.WriteLine("{0}\n{1}\n{2}", a, b, c);
    }
}